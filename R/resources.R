#' Function to extract imperial colors as hex codes
#'
#' @param ... Character names of imperial_colors
#'
imperial_cols <- function(...) {
  cols <- c(...)
  if (is.null(cols))
    return (imperial_colours)
  imperial_colours[cols]
}

